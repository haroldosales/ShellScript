
#!/usr/bin/env bash

# ------------------------------------------------------------------------ #

# ------------------------------- VARIÁVEIS --------------------------------#

ARQUIVOS_TITULOS="texto.txt"

VERDE="\033[32;1m"

PRETO="\033[30;1m"

# ------------------------------------------------------------------------ #

#--------------------- FUNÇÕES ----------------------------------------- #

#--------------------- TESTE ----------------------------------------- #

[ ! -x "$(which lynx)" ] && sudo pacman -S lynx -y # liynx instalado

# ------------------------------------------------------------------------ #

# ------------------------------- EXECUÇÃO ----------------------------------------- #

lynx -source http://haroldosales.com/  | grep m-0  | sed 's/<div.*line">//;s/<item.*//'  > texto.txt

while read -r texto_lxer

do

echo -e "${VERDE}Titulo: ${PRETO}$texto_lxer"

done < "$ARQUIVOS_TITULOS"

