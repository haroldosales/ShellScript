# ------------------------------- VARIÁVEIS --------------------------------#
CHAVE_DEBUG=0
NIVEL_DEBUG=0

# ------------------------------------------------------------------------ #



Debugar () {
  [ $1 -le $NIVEL_DEBUG ] && echo "Debug $* ------"
}
#--------------------- FUNÇÕES ----------------------------------------- #

SOMA () {
    local teste=1

  for (( i = 1; i < 10; i++ )); do
    Debugar 1 "Entre  i no for com o valor: $i"
        teste=$(($teste+$i))
        Debugar 2 "Depois da soma: $teste"
  done
  echo $teste
}
# ------------------------------------------------------------------------ #

case "$1" in
  -d) [ $2 ] && NIVEL_DEBUG=$2 ;;
   *) Soma                     ;;
esac
# ------------------------------- EXECUÇÃO ----------------------------------------- #
SOMA
